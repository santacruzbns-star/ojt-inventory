     * Display a listing of the resource.
     */
    public function index(Request $request)
    {
        $query = $request->get('search', '');
        $remarkFilter = $request->get('remark');
        $categoryId = $request->get('category');
        $brandId = $request->get('brand');

        $item_categories = ItemCategory::all();
        $item_brands = ItemBrand::all();
        $item_uoms = ItemUom::all();

        $itemCount = Item::count();

        // Base query for items
        $itemsQuery = Item::query();

        if ($query) {
            $itemsQuery->where(function ($q) use ($query) {
                $q->where('item_name', 'like', "%$query%")
                    ->orWhere('item_serialno', 'like', "%$query%");
            });
        }

        if ($remarkFilter) {
            $itemsQuery->where('item_remark', $remarkFilter);
        }

        if ($categoryId) {
            $itemsQuery->where('item_category_id', $categoryId);
        }

        if ($brandId) {
            $itemsQuery->where('item_brand_id', $brandId);
        }

        // Get regular items (eager-load for PDF / table display)
        $items = $itemsQuery->with(['category', 'uom'])->orderBy('updated_at', 'desc')->get();

        // Paginate manually (using the collection of regular items)
        $perPage = 20;
        $page = $request->get('page', 1);
        $itemsPaginated = new \Illuminate\Pagination\LengthAwarePaginator(
            $items->forPage($page, $perPage),
            $items->count(),
            $perPage,
            $page,
            ['path' => $request->url(), 'query' => $request->query()]
        );

        $itemsPaginated->appends([
            'search' => $query,
            'remark' => $remarkFilter,
            'category' => $categoryId,
            'brand' => $brandId
        ]);

        // Get unique remarks for dropdown (only from the Item table now)
        $item_remarks = Item::select('item_remark')
            ->distinct()
            ->orderBy('item_remark')
            ->pluck('item_remark')
            ->toArray();

        // EXPORT SINGLE ITEM PDF
        if ($request->get('export') == 'pdf' && $request->has('item_id')) {
            $itemId = $request->get('item_id');
            $item = Item::findOrFail($itemId);
            $pdf = Pdf::loadView('inventory.pdf-individual', compact('item'));
            return $pdf->stream("item_{$item->item_id}.pdf");
        }

        // EXPORT LIST PDF (all rows matching filters, or only selected ids)
        if ($request->get('export') == 'pdf' && !$request->has('item_id')) {
            $ids = $request->get('ids');

            if ($ids) {
                $idArray = array_filter(explode(',', $ids));
                $pdfItems = Item::with(['category', 'uom'])
                    ->whereIn('item_id', $idArray)
                    ->get();
            } else {
                $pdfItems = $items;
            }

            // 🔥 SORT FOR PDF: Group by Category
            $pdfItems = $pdfItems->sortBy('category.item_category_name', SORT_NATURAL | SORT_FLAG_CASE)->values();

            // Load view and set Landscape
            $pdf = Pdf::loadView('inventory.pdf-forall', ['allItems' => $pdfItems])
                ->setPaper('a4', 'landscape');

            // Render PDF to initialize canvas
            $pdf->render();
            $canvas = $pdf->getDomPDF()->getCanvas();

            // Script for Footer and Page Numbers
            $script = <<<'EOT'
            $font = $fontMetrics->get_font("Helvetica", "normal");
            $size = 10;

            if ($PAGE_NUM === $PAGE_COUNT) {
                $footerText = "Generated by Goldtown Inventory System " . date('F d, Y');
                $footerWidth = $fontMetrics->getTextWidth($footerText, $font, $size);
                $xFooter = ($pdf->get_width() - $footerWidth) / 2;
                $yFooter = $pdf->get_height() - (2.5 * 12);
                $pdf->text($xFooter, $yFooter, $footerText, $font, $size);
            }

            // Page number at bottom-right
            $pageText = "Page " . $PAGE_NUM;
            $pageWidth = $fontMetrics->getTextWidth($pageText, $font, $size);
            $xPage = $pdf->get_width() - $pageWidth - (2 * 12);
            $yPage = $pdf->get_height() - (2.5 * 12);
            $pdf->text($xPage, $yPage, $pageText, $font, $size);
EOT;
            $canvas->page_script($script);

            return $pdf->stream('inventory.pdf');
        }

        // AJAX Table Update
        if ($request->ajax()) {
            return response()->json([
                'table' => view('inventory.inventory-table', [
                    'items' => $itemsPaginated,
                    'item_categories' => $item_categories,
                    'item_brands' => $item_brands,
                    'item_uoms' => $item_uoms,
                ])->render(),
            ]);
        }

        // EXPORT EXCEL
        if ($request->get('export') == 'excel') {
            $ids = $request->get('ids');
            $dbQuery = Item::with(['category', 'uom', 'brand']); // Ensure eager loading

            if ($ids) {
                $idArray = explode(',', $ids);
                $dbQuery->whereIn('item_id', $idArray);
            } else {
                if ($query) {
                    $dbQuery->where(function ($q) use ($query) {
                        $q->where('item_name', 'like', "%$query%")
                            ->orWhere('item_serialno', 'like', "%$query%");
                    });
                }
                if ($remarkFilter) {
                    $dbQuery->where('item_remark', $remarkFilter);
                }
                if ($categoryId) {
                    $dbQuery->where('item_category_id', $categoryId);
                }
                if ($brandId) {
                    $dbQuery->where('item_brand_id', $brandId);
                }
            }

            // 🔥 SORT FOR EXCEL: Group by Category
            $filteredItems = $dbQuery->get()->sortBy('category.item_category_name', SORT_NATURAL | SORT_FLAG_CASE)->values();

            return Excel::download(new ItemsExport($filteredItems), 'Item Inventory Report.xlsx');
        }

        return view('inventory.index', [
            'items' => $itemsPaginated,
            'item_categories' => $item_categories,
            'item_brands' => $item_brands,
            'item_uoms' => $item_uoms,
            'item_remarks' => $item_remarks,
            'itemCount' => $itemCount
        ]);
    }

