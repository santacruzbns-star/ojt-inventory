    /**
     * Update the specified resource in storage.
     */
    public function update(Request $request, $item_id)
    {
        $item = Item::findOrFail($item_id);

        $itemQuantityRules = ['required', 'integer', 'min:0'];
        if ($request->filled('item_serialno')) {
            $itemQuantityRules[] = 'max:1';
        }

        // Validate input
        $request->validate([
            'item_name' => 'required|string|max:255',
            'item_serialno' => 'nullable|string|max:255',
            'item_quantity' => $itemQuantityRules,
            'item_remark' => 'nullable|string|max:500',
            'item_brand_name' => 'nullable|string|max:255',
            'item_uom_name' => 'nullable|string|max:255',
        ]);

        // Serialized items are always qty 1; otherwise total quantity cannot decrease
        if (
            !$request->filled('item_serialno')
            && $request->item_quantity < $item->item_quantity
        ) {
            return redirect()->back()->with('error', "Cannot decrease total quantity below current total ({$item->item_quantity}).");
        }

        // Calculate how much the total increased
        $difference = $request->item_quantity - $item->item_quantity;

        // Increment remaining stock by the difference
        $item->item_quantity_remaining += $difference;

        // Update total quantity
        $item->item_quantity = $request->item_quantity;

        // Update other fields
        $item->item_name = $request->item_name;
        $item->item_serialno = $request->item_serialno;
        $item->item_remark = $request->item_remark;

        // Update Brand if provided
        if ($request->item_brand_name) {
            $brand = ItemBrand::firstOrCreate([
                'item_brand_name' => $request->item_brand_name
            ]);
            $item->item_brand_id = $brand->item_brand_id;
        }

        // Update Unit of Measure if provided
        if ($request->item_uom_name) {
            $uom = ItemUom::firstOrCreate([
                'item_uom_name' => $request->item_uom_name
            ]);
            $item->item_uom_id = $uom->item_uom_id;
        }

        // Update quantity status
        if ($item->item_quantity_remaining == 0) {
            $item->item_quantity_status = 'Out of Stock';
        } elseif ($item->item_quantity_remaining < ($item->item_quantity * 0.2)) {
            $item->item_quantity_status = 'Low Stock';
        } else {
            $item->item_quantity_status = 'Available';
        }

        $item->save();

        return redirect()->back()->with('success', 'Item updated successfully.');
    }
    /**
     * Remove the specified resource from storage.
     */
    public function destroy(Request $request, $item_id)
    {
        $item = Item::findOrFail($item_id);
        $deleteQty = $request->input('delete_qty');

        // If user entered a number and it's less than the total remaining
        if (!empty($deleteQty) && is_numeric($deleteQty) && $deleteQty < $item->item_quantity_remaining) {

            $item->item_quantity -= $deleteQty;
            $item->item_quantity_remaining -= $deleteQty;

            // Recalculate status logic
            if ($item->item_quantity_remaining == 0) {
                $item->item_quantity_status = 'Out of Stock';
            } elseif ($item->item_quantity_remaining < ($item->item_quantity * 0.2)) {
                $item->item_quantity_status = 'Low Stock';
            } else {
                $item->item_quantity_status = 'Available';
            }

            $item->save();

            return response()->json([
                'success' => true,
                'deleted_entirely' => false,
                'message' => "Removed $deleteQty items from stock."
            ]);
        }

        // Otherwise, delete the entire record
        $item->delete();

        return response()->json([
            'success' => true,
            'deleted_entirely' => true,
            'message' => 'Item deleted successfully.'
        ]);
    }

    public function destroyCategory($id)
    {
        $category = ItemCategory::findOrFail($id);

        // Check if there are any items using this category
        $itemCount = Item::where('item_category_id', $id)->count();

        if ($itemCount > 0) {
            return response()->json([
                'success' => false,
                'message' => "Cannot delete category. There are still $itemCount item(s) assigned to it."
            ], 422); // 422 Unprocessable Entity
        }

        $category->delete();

        return response()->json([
            'success' => true,
            'message' => 'Category deleted successfully.'
        ]);
    }

    public function bulkDestroy(Request $request)
    {
        $ids = $request->input('ids', []);
        $selectAll = $request->input('all', false); // New flag from JS

        // 1. Logic for "Select All" (Across all pages)
        if ($selectAll) {
            $query = Item::query();

            // Apply same filters as your index page so you don't delete 
            // things the user isn't currently looking at!
            if ($request->has('category') && $request->category != '') {
                $query->where('item_category_id', $request->category);
            }
            if ($request->has('brand') && $request->brand != '') {
                $query->where('item_brand_id', $request->brand);
            }
            if ($request->has('search') && $request->search != '') {
                $query->where('item_name', 'like', '%' . $request->search . '%');
            }

            $query->delete();
            return response()->json(['success' => true, 'message' => 'All filtered items deleted.']);
        }

        // 2. Logic for "Selected Page Items"
        if (!empty($ids)) {
            Item::whereIn('item_id', $ids)->delete();
            return response()->json(['success' => true, 'message' => 'Selected items deleted successfully.']);
        }

        return response()->json(['success' => false, 'message' => 'No items selected.'], 400);
    }
